/**
 * student-dashboard.js — Student dashboard logic
 */

document.addEventListener('DOMContentLoaded', async () => {
  // Auth guard
  const user = requireAuth();
  if (!user) return;

  // Inject components
  await injectComponents('Student Dashboard');

  // Set greeting
  const hour = new Date().getHours();
  const greet = hour < 12 ? 'Good morning' : hour < 17 ? 'Good afternoon' : 'Good evening';
  document.getElementById('welcomeTitle').textContent = `${greet}, ${user.name.split(' ')[0]} 👋`;

  // Load requests
  loadStudentStats();
  loadRecentRequests();
});

async function loadStudentStats() {
  try {
    const res = await apiCall('/lor/requests', 'GET');
    if (!res.success) return;

    const requests = res.data;
    document.getElementById('statTotal').textContent    = requests.length;
    document.getElementById('statApproved').textContent = requests.filter(r => r.status === 'approved').length;
    document.getElementById('statPending').textContent  = requests.filter(r => r.status === 'pending').length;
    document.getElementById('statRejected').textContent = requests.filter(r => r.status === 'rejected').length;
  } catch (err) {
    console.error('Failed to load stats:', err);
  }
}

async function loadRecentRequests() {
  const tbody = document.getElementById('recentRequestsBody');
  try {
    const res = await apiCall('/lor/requests', 'GET');
    if (!res.success || !res.data.length) {
      tbody.innerHTML = '<tr><td colspan="6" class="text-center text-muted py-4">No requests found.</td></tr>';
      return;
    }

    // Show latest 3
    const recent = res.data.slice(0, 3);
    tbody.innerHTML = recent.map((r, i) => `
      <tr>
        <td class="fw-700">#${r.id}</td>
        <td>${r.faculty}</td>
        <td>${r.purpose}</td>
        <td>${formatDate(r.submittedAt)}</td>
        <td><span class="badge-status badge-${r.status}">${capitalize(r.status)}</span></td>
        <td>
          ${r.status === 'approved'
            ? `<button class="btn btn-sm btn-outline-primary" onclick="downloadLOR(${JSON.stringify(r).replace(/"/g, '&quot;')})">
                <i class="bi bi-download me-1"></i>PDF
               </button>`
            : '<span class="text-muted" style="font-size:.8rem;">—</span>'
          }
        </td>
      </tr>
    `).join('');
  } catch (err) {
    tbody.innerHTML = '<tr><td colspan="6" class="text-center text-danger py-4">Failed to load requests.</td></tr>';
    console.error(err);
  }
}

/** Generate and download LOR PDF using jsPDF */
function downloadLOR(request) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();
  const user = getSessionUser();

  const today = new Date().toLocaleDateString('en-IN', { day: '2-digit', month: 'long', year: 'numeric' });

  doc.setFont('helvetica', 'bold');
  doc.setFontSize(16);
  doc.text('Letter of Recommendation', 105, 20, { align: 'center' });

  doc.setFontSize(11);
  doc.setFont('helvetica', 'normal');
  doc.text(`Date: ${today}`, 20, 40);
  doc.text(`To Whom It May Concern,`, 20, 55);

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10.5);
  const body = doc.splitTextToSize(
    `I am pleased to recommend ${request.studentName} (USN: ${request.usn}), a student of the ${request.branch} ` +
    `department, for the purpose of ${request.purpose}. This letter has been issued upon request ` +
    `and duly approved by ${request.faculty}.`,
    170
  );
  doc.text(body, 20, 70);

  doc.text(`Yours sincerely,`, 20, 110);
  doc.setFont('helvetica', 'bold');
  doc.text(request.faculty, 20, 120);
  doc.setFont('helvetica', 'normal');
  doc.text(`Department of ${request.branch}`, 20, 128);

  doc.setFontSize(9);
  doc.setTextColor(150);
  doc.text(`Generated by LOR Portal — ${today}`, 105, 280, { align: 'center' });

  doc.save(`LOR_${request.studentName.replace(/\s/g, '_')}_${request.id}.pdf`);
}

// ============================================================
// Component injection utility (shared across dashboard pages)
// ============================================================

async function injectComponents(pageTitle = 'Dashboard') {
  const user = getSessionUser();

  // Inject sidebar
  const sidebarEl = document.getElementById('sidebar-placeholder');
  if (sidebarEl) {
    const html = await fetch('../components/sidebar.html').then(r => r.text());
    sidebarEl.innerHTML = html;
    setupSidebar(user);
  }

  // Inject navbar
  const navbarEl = document.getElementById('navbar-placeholder');
  if (navbarEl) {
    const html = await fetch('../components/navbar.html').then(r => r.text());
    navbarEl.innerHTML = html;
    setupNavbar(user, pageTitle);
  }

  // Inject footer
  const footerEl = document.getElementById('footer-placeholder');
  if (footerEl) {
    const html = await fetch('../components/footer.html').then(r => r.text());
    footerEl.innerHTML = html;
  }
}

function setupSidebar(user) {
  if (!user) return;
  const role = user.role; // student | faculty | coordinator

  // Show/hide role-specific links
  document.querySelectorAll('[class*="role-"]').forEach(el => {
    const forRole = [...el.classList].find(c => c.startsWith('role-'))?.replace('role-', '');
    if (forRole && forRole !== role) el.style.display = 'none';
  });

  // Active link
  const current = window.location.pathname.split('/').pop().replace('.html', '');
  document.querySelectorAll('.sidebar-link[data-page]').forEach(link => {
    if (link.dataset.page === current) link.classList.add('active');
  });

  // User card
  document.getElementById('sidebarUserName').textContent = user.name;
  document.getElementById('sidebarUserRole').textContent = capitalize(role);
  document.getElementById('sidebarAvatarInitial').textContent = user.name[0].toUpperCase();
}

function setupNavbar(user, pageTitle) {
  document.getElementById('topbarTitle').textContent = pageTitle;
  document.getElementById('navUserName').textContent = user?.name || 'User';
  document.getElementById('navUserRole').textContent = capitalize(user?.role || '');
  const avatarBtn = document.getElementById('navAvatarBtn');
  if (avatarBtn && user) avatarBtn.textContent = user.name[0].toUpperCase();
}

function toggleSidebar() {
  document.getElementById('sidebar').classList.toggle('open');
}

// ============================================================
// Utility helpers
// ============================================================

function capitalize(str) {
  if (!str) return '';
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function formatDate(dateStr) {
  if (!dateStr) return '—';
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
}